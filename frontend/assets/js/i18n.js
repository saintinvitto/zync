const Lang = {
  IDIOMAS: ['pt', 'en', 'es'],

  get() {
    const salvo = localStorage.getItem('idioma');
    return this.IDIOMAS.includes(salvo) ? salvo : 'pt';
  },

  set(idioma) {
    localStorage.setItem('idioma', idioma);
    this.aplicar();
  },

  t(chave, vars) {
    const texto = TRADUCOES[this.get()]?.[chave] || TRADUCOES.pt[chave] || chave;
    if (!vars) return texto;
    return Object.entries(vars).reduce((acc, [k, v]) => acc.replaceAll(`{${k}}`, v), texto);
  },

  aplicar() {
    document.documentElement.lang = this.get() === 'pt' ? 'pt-BR' : this.get();

    document.querySelectorAll('[data-i18n]').forEach((el) => {
      el.textContent = this.t(el.dataset.i18n);
    });

    document.querySelectorAll('[data-i18n-placeholder]').forEach((el) => {
      el.placeholder = this.t(el.dataset.i18nPlaceholder);
    });
  },
};
